const putTableData = (req, res, db) => {
  const { id, name, Adress, email, Web, phone } = req.body
  db('fournisseurs').where({id}).update({id, name, Adress, email, Web, phone })
    .returning('*')
    .then(item => {
      res.json(item)
    })
    .catch(err => res.status(400).json({dbError: 'db error'}))
}

const deleteTableData = (req, res, db) => {
  const { id } = req.body
  db('fournisseurs').where({id}).del()
    .then(() => {
      res.json({delete: 'true'})
    })
    .catch(err => res.status(400).json({dbError: 'db error'}))
}

